
import React, { useEffect } from 'react';
import { Link, Navigate } from 'react-router-dom';
import { AlertTriangle, CheckCircle, ArrowLeft, Info, History } from 'lucide-react';
import { useApp } from '../context/AppContext';

const Results = () => {
  const { uploadedImage, analysisResults, canAccessResults, setCanAccessResults } = useApp();

  // Reset access permission when component unmounts (user navigates away)
  useEffect(() => {
    return () => {
      setCanAccessResults(false);
    };
  }, [setCanAccessResults]);

  // Redirect if no analysis has been performed or direct access is attempted
  if (!uploadedImage || analysisResults.length === 0 || !canAccessResults) {
    return <Navigate to="/upload" replace />;
  }

  const getConfidenceColor = (confidence: number) => {
    if (confidence >= 80) return 'text-green-600 bg-green-50';
    if (confidence >= 60) return 'text-yellow-600 bg-yellow-50';
    return 'text-red-600 bg-red-50';
  };

  const getConfidenceIcon = (confidence: number) => {
    if (confidence >= 80) return <CheckCircle className="h-5 w-5" />;
    return <AlertTriangle className="h-5 w-5" />;
  };

  return (
    <div className="min-h-screen bg-gray-50 py-8">
      <div className="max-w-6xl mx-auto px-4 sm:px-6 lg:px-8">
        <div className="mb-8">
          <Link
            to="/upload"
            className="inline-flex items-center text-blue-600 hover:text-blue-700 font-medium mb-6"
          >
            <ArrowLeft className="h-4 w-4 mr-2" />
            Back to Upload
          </Link>
          
          <div className="text-center">
            <h1 className="text-3xl lg:text-4xl font-bold text-gray-900 mb-4">
              Analysis Results
            </h1>
            <p className="text-lg text-gray-600 max-w-2xl mx-auto">
              Our AI has analyzed your image and identified the following potential skin conditions
            </p>
          </div>
        </div>

        <div className="grid grid-cols-1 lg:grid-cols-3 gap-8">
          {/* Image Preview */}
          <div className="lg:col-span-1">
            <div className="bg-white rounded-xl shadow-sm p-6 sticky top-8">
              <h2 className="text-xl font-semibold text-gray-900 mb-4">
                Uploaded Image
              </h2>
              <img
                src={uploadedImage}
                alt="Analyzed skin image"
                className="w-full rounded-lg shadow-sm"
              />
            </div>
          </div>

          {/* Results */}
          <div className="lg:col-span-2 space-y-6">
            {analysisResults.map((result, index) => (
              <div key={index} className="bg-white rounded-xl shadow-sm p-6">
                <div className="flex items-start justify-between mb-4">
                  <div className="flex-1">
                    <h3 className="text-2xl font-bold text-gray-900 mb-2">
                      {result.name}
                    </h3>
                    <p className="text-gray-600 mb-4">
                      {result.description}
                    </p>
                  </div>
                  <div className={`flex items-center px-3 py-1 rounded-full ${getConfidenceColor(result.confidence)}`}>
                    {getConfidenceIcon(result.confidence)}
                    <span className="ml-1 font-semibold">
                      {result.confidence}%
                    </span>
                  </div>
                </div>

                <div className="bg-blue-50 border border-blue-200 rounded-lg p-4">
                  <div className="flex items-start">
                    <Info className="h-5 w-5 text-blue-600 mr-3 mt-0.5 flex-shrink-0" />
                    <div>
                      <h4 className="font-semibold text-blue-900 mb-2">
                        Recommendation
                      </h4>
                      <p className="text-blue-800">
                        {result.recommendation}
                      </p>
                    </div>
                  </div>
                </div>
              </div>
            ))}

            {/* Medical Disclaimer */}
            <div className="bg-yellow-50 border border-yellow-200 rounded-xl p-6">
              <div className="flex items-start">
                <AlertTriangle className="h-6 w-6 text-yellow-600 mr-4 mt-0.5 flex-shrink-0" />
                <div>
                  <h3 className="text-lg font-semibold text-yellow-900 mb-2">
                    Important Medical Notice
                  </h3>
                  <p className="text-yellow-800 mb-4">
                    These results are generated by AI for educational purposes only and should not be considered 
                    as professional medical advice, diagnosis, or treatment recommendations.
                  </p>
                  <ul className="text-yellow-800 space-y-1 list-disc list-inside text-sm">
                    <li>Always consult with qualified healthcare professionals</li>
                    <li>Do not delay seeking professional medical advice</li>
                    <li>AI analysis may not capture all medical complexities</li>
                  </ul>
                </div>
              </div>
            </div>

            {/* Action Buttons */}
            <div className="flex flex-col sm:flex-row gap-4">
              <Link
                to="/upload"
                className="flex-1 bg-blue-600 text-white text-center px-6 py-3 rounded-lg font-semibold hover:bg-blue-700 transition-colors"
              >
                Analyze Another Image
              </Link>
              <Link
                to="/history"
                className="flex-1 border border-gray-300 text-gray-700 text-center px-6 py-3 rounded-lg font-semibold hover:bg-gray-50 transition-colors flex items-center justify-center"
              >
                <History className="h-4 w-4 mr-2" />
                View History
              </Link>
              <Link
                to="/contact"
                className="flex-1 border border-gray-300 text-gray-700 text-center px-6 py-3 rounded-lg font-semibold hover:bg-gray-50 transition-colors"
              >
                Find Healthcare Providers
              </Link>
            </div>
          </div>
        </div>
      </div>
    </div>
  );
};

export default Results;
